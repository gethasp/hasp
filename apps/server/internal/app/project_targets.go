func renderProjectExamples(w io.Writer, projectRoot string, results []projectExampleResult, wrote bool) error {
	title := "Project examples"
	lead := "Checked repo-managed example files without writing."
	if wrote {
		lead = "Wrote repo-managed example files."
	}
	if err := cliWriteStage(w, title, lead); err != nil {
		return err
	}
	if err := cliWriteKeyValues(w, "Project",
		cliPair("Project root", cliDisplayPath(projectRoot)),
		cliPair("Write mode", setupYesNo(wrote)),
	); err != nil {
		return err
	}
	if err := cliSection(w, "Targets"); err != nil {
		return err
	}
	if len(results) == 0 {
		_, err := fmt.Fprintln(w, "  none")
		return err
	}
	for _, result := range results {
		outcome := "ok"
		switch {
		case result.Written:
			outcome = "written"
		case result.Missing:
			outcome = "missing"
		case result.Stale:
			outcome = "stale"
		}
		if _, err := fmt.Fprintln(w, cliBullet(w, fmt.Sprintf("%-22s", result.Target), cliOutcome(w, outcome))); err != nil {
			return err
		}
	}
	return nil
}

func renderProjectDoctor(w io.Writer, projectRoot string, diagnostics []projectDoctorDiagnostic) error {
	lead := fmt.Sprintf("Collected %d safe project-target diagnostics.", len(diagnostics))
	if err := cliWriteStage(w, "Project doctor", lead); err != nil {
		return err
	}
	if err := cliWriteKeyValues(w, "Project",
		cliPair("Project root", cliDisplayPath(projectRoot)),
	); err != nil {
		return err
	}
	if err := cliSection(w, "Diagnostics"); err != nil {
		return err
	}
	if len(diagnostics) == 0 {
		_, err := fmt.Fprintln(w, "  none")
		return err
	}
	for _, diag := range diagnostics {
		label := diag.Code
		if diag.Ref != "" {
			label = diag.Ref
		} else if diag.Target != "" {
			label = diag.Target
		}
		details := []string{
			setupSummaryValue(w, diag.Code),
			setupSummaryValue(w, diag.Severity),
		}
		if diag.Kind != "" {
			details = append(details, setupSummaryValue(w, string(diag.Kind)))
		}
		if diag.Classification != "" {
			details = append(details, setupSummaryValue(w, diag.Classification))
		}
		if diag.Present {
			details = append(details, cliMuted(w, "(present true)"))
		}
		if diag.Exposed {
			details = append(details, cliMuted(w, "(exposed true)"))
		}
		if diag.Stale {
			details = append(details, cliMuted(w, "(stale true)"))
		}
		if diag.Ignored {
			details = append(details, cliMuted(w, "(ignored true)"))
		}
		if _, err := fmt.Fprintln(w, cliBullet(w, fmt.Sprintf("%-22s", label), details...)); err != nil {
			return err
		}
	}
	return nil
}

func renderProjectManifestWriteResult(w io.Writer, title string, lead string, result projectManifestWriteResult) error {
	if err := cliWriteStage(w, title, lead); err != nil {
		return err
	}
	if err := cliWriteKeyValues(w, "Manifest",
		cliPair("Project root", cliDisplayPath(result.ProjectRoot)),
		cliPair("Manifest", cliDisplayPath(result.ManifestPath)),
		cliPair("Created", setupYesNo(result.Created)),
		cliPair("Updated", setupYesNo(result.Updated)),
	); err != nil {
		return err
	}
	if strings.TrimSpace(result.Target) != "" {
		if err := cliWriteKeyValues(w, "Target",
			cliPair("Name", result.Target),
			cliPair("Refs", strings.Join(result.Refs, ", ")),
			cliPair("Command", setupYesNo(result.Command)),
		); err != nil {
			return err
		}
	}
	if _, err := fmt.Fprintln(w, "Next steps"); err != nil {
		return err
	}
	if result.Target == "" {
		_, err := fmt.Fprintln(w, "  hasp project target add <name> --env NAME=@REF -- <command>")
		return err
	}
	if _, err := fmt.Fprintf(w, "  hasp project doctor --project-root %s\n", strconv.Quote(result.ProjectRoot)); err != nil {
		return err
	}
	_, err := fmt.Fprintf(w, "  hasp run --project-root %s --target %s --explain --dry-run\n", strconv.Quote(result.ProjectRoot), strconv.Quote(result.Target))
	return err
}
