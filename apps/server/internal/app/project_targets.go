func projectExamplesCommand(ctx context.Context, args []string, stdout io.Writer) error {
	fs := flag.NewFlagSet("project examples", flag.ContinueOnError)
	fs.SetOutput(io.Discard)
	jsonOutput := fs.Bool("json", false, "")
	projectRoot := fs.String("project-root", ".", "")
	targetName := fs.String("target", "", "")
	check := fs.Bool("check", false, "")
	write := fs.Bool("write", false, "")
	if err := fs.Parse(args); err != nil {
		return err
	}
	if fs.NArg() != 0 {
		return errors.New("usage: hasp project examples --project-root <path> [--target <name>] --check|--write")
	}
	if *check == *write {
		return errors.New("choose exactly one of --check or --write")
	}

	root, manifest, err := loadProjectManifest(*projectRoot)
	if err != nil {
		return err
	}

	targets, err := selectedManifestTargets(manifest, strings.TrimSpace(*targetName))
	if err != nil {
		return err
	}
	results, err := projectExampleActions(root, manifest, targets, *write)
	if err != nil {
		return err
	}
	payload := map[string]any{"results": results}
	return renderJSONOrHuman(ctx, stdout, *jsonOutput, payload, func(w io.Writer) error {
		return renderProjectExamples(w, root, results, *write)
	})
}

