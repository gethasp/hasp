const (
	projectExampleGeneratedMarker = "hasp-generated by hasp project examples"
	projectExampleSecretValue     = "__HASP_SECRET__"
	projectExamplePublicValue     = "__HASP_PUBLIC_CONFIG__"
	projectExampleFileValue       = "__HASP_FILE__"
)

type projectRequirementView struct {
	Ref              string         `json:"ref"`
	Kind             store.ItemKind `json:"kind"`
	Classification   string         `json:"classification"`
	Required         bool           `json:"required"`
	Present          bool           `json:"present"`
	Exposed          bool           `json:"exposed"`
	VaultAvailable   bool           `json:"vault_available"`
	Targets          []string       `json:"targets,omitempty"`
	SuggestedCommand string         `json:"suggested_command,omitempty"`
}

type projectTargetView struct {
	Name         string   `json:"name"`
	Refs         []string `json:"refs,omitempty"`
	HasCommand   bool     `json:"has_command"`
	HasEnv       bool     `json:"has_env"`
	HasFiles     bool     `json:"has_files"`
	HasXCConfig  bool     `json:"has_xcconfig"`
	HasExamples  bool     `json:"has_examples"`
	HasEnvSample bool     `json:"has_env_example"`
	HasXCSample  bool     `json:"has_xcconfig_example"`
}

type projectExampleResult struct {
	Target  string `json:"target"`
	Missing bool   `json:"missing"`
	Stale   bool   `json:"stale"`
	Written bool   `json:"written"`
}

type projectDoctorDiagnostic struct {
	Code           string         `json:"code"`
	Severity       string         `json:"severity"`
	Target         string         `json:"target,omitempty"`
	Ref            string         `json:"ref,omitempty"`
	Kind           store.ItemKind `json:"kind,omitempty"`
	Classification string         `json:"classification,omitempty"`
	Present        bool           `json:"present,omitempty"`
	Exposed        bool           `json:"exposed,omitempty"`
	Ignored        bool           `json:"ignored,omitempty"`
	Stale          bool           `json:"stale,omitempty"`
}

func projectRequirementsCommand(ctx context.Context, args []string, stdout io.Writer) error {
	fs := flag.NewFlagSet("project requirements", flag.ContinueOnError)
	fs.SetOutput(io.Discard)
	jsonOutput := fs.Bool("json", false, "")
	projectRoot := fs.String("project-root", ".", "")
	targetName := fs.String("target", "", "")
	if err := fs.Parse(args); err != nil {
		return err
	}
	if fs.NArg() != 0 {
		return errors.New("usage: hasp project requirements --project-root <path> [--target <name>] [--json]")
	}

	root, manifest, err := loadProjectManifest(*projectRoot)
	if err != nil {
		return err
	}

	reqs, err := projectRequirementViews(ctx, root, manifest, strings.TrimSpace(*targetName))
	if err != nil {
		return err
	}
	payload := map[string]any{"requirements": reqs}
	return renderJSONOrHuman(ctx, stdout, *jsonOutput, payload, func(w io.Writer) error {
		return renderProjectRequirements(w, root, strings.TrimSpace(*targetName), reqs)
	})
}

